<?php
$nilai1 = 13;
$nilai2 = 5;

// Perkalian
$hasilKali = $nilai1 * $nilai2;
echo "Hasil perkalian $nilai1 dan $nilai2 adalah: " . $hasilKali;
echo '<br/>';

// Penjumlahan
$hasilJumlah = $nilai1 + $nilai2;
echo "Hasil penjumlahan $nilai1 dan $nilai2 adalah: " . $hasilJumlah;
echo '<br/>';

// Pembagian
$hasilBagi = $nilai1 / $nilai2;
echo "Hasil pembagian $nilai1 dan $nilai2 adalah: " . $hasilBagi;
echo '<br/>';

// Pengurangan
$hasilKurang = $nilai1 - $nilai2;
echo "Hasil pengurangan $nilai1 dan $nilai2 adalah: " . $hasilKurang;
echo '<br/>';

// Modulus (sisa pembagian)
$hasilModulus = $nilai1 % $nilai2;
echo "Hasil modulus (sisa pembagian) $nilai1 dan $nilai2 adalah: " . $hasilModulus;